	<h3>1. What is ... this ?</h3>
	<a name="question1"></a>
	<p>
		This is an opensource library for reports written in java.<br>
		Some of the features of this java reports library are: 
<ul>
			<li><strong>Easy to use and understand</strong> - you only need 5 minutes to undertand and make a simple report</li>
			<li><strong>Reliable design</strong> - obtained by segregation of the the main layers : Input - Algorithm - Output</li>
			<li><strong>Superior speed</strong> - compared with orther products of this kind, ReportEngine relies on very fast algorithms</li>
			<li><strong>No precompilation</strong> - you don't have to lose your precious time precompiling. <br>The engine only needs 
			a basic configuration which can be done via its setter methods.</li>
</ul>	
		The author of this project is <a href="mailto: dragos.balan@gmail.com">Dragos Balan</a>.
	</p>
	
